package day04_dataCasting;

public class C05_Pre_post_Increment {
    public static void main(String[] args) {
        int sayi1 = 10;
        int sayi2 = sayi1 + 1;
        int sayi3 = sayi2++;
        int sayi4 = ++sayi1;
        System.out.println(sayi3);
        System.out.println(sayi4);


    }


}
